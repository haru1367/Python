# 정수 변수 a와 b를 선언하고 값을 할당
a = 3
b = 4

# a와 b를 출력
print(a, b)

# 실수 변수 a와 b를 선언하고 값을 할당
a = 1.3
b = 2.2

# a와 b를 출력
print(a, b)

# 문자열 변수 a와 b를 선언하고 값을 할당
a = "ABC"
b = "1092"

# a와 b를 출력
print(a, b)

# 부울 변수 a와 b를 선언하고 값을 할당
a = True
b = False

# a와 b를 출력
print(a, b)

# 정수 변수 a와 b를 선언하고 값을 할당
a = 52
b = 7

# 더하기, 빼기, 곱하기, 나누기, 지수 계산, 정수 나누기, 나머지 계산
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a ** b)
print(a // b)
print(a % b)

# a에 1을 더하고 출력
a += 1
print(a)

# a에서 1을 빼고 출력
a -= 1
print(a)

# 정수 변수 a를 선언하고 값을 할당
a = 5

# a를 출력
print(a)

# a를 실수로 변환하여 저장하고 출력
a = float(5)
print(a)

# 실수를 정수로 변환하고 저장
a = int(10.3)
print(a)

# 문자열 a를 실수로 변환하고 b에 저장
a = "195.326"
b = float(a)
print(b)

# 변수 타입 출력
print(type(a))
print(type(b))